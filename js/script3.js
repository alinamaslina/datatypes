// Запрашиваем первую строку у пользователя
let string1 = prompt("Введите первую строку:");

// Запрашиваем вторую строку у пользователя
let string2 = prompt("Введите вторую строку:");

// Сравниваем строки с помощью оператора ==
if (string1 == string2) {
    alert("Строки равны ");
} else {
    alert("Строки не равны ");
}

// Сравниваем строки с помощью оператора ===
if (string1 === string2) {
    alert("Строки равны ");
} else {
    alert("Строки не равны ");
}